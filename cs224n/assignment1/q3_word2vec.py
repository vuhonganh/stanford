def normalizeRows(x):
    """ Row normalization function

    Implement a function that normalizes each row of a matrix to have
    unit length.
    """
    x_norm = np.sqrt(np.sum(x ** 2, axis=1, keepdims=True))
    x = x / x_norm
    return x


